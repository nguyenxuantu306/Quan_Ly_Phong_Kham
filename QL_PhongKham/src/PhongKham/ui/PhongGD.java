    void init(){
        this.row = -1;
        initTable();
        this.fillTable(); 
        this.initcombobox();
    }
    
    public void initTable(){
        DefaultTableModel model = (DefaultTableModel) tblDanhSachPhong.getModel();
        model.setColumnIdentifiers(new String[]{"MaPhong","TenPhong","GiaPhong","SoGiuong","LoaiPhong"});
    }
    
     public void initcombobox (){ 
        String[] loaiphong = {"Tiêu chuẩn","VIP","Phòng tiện ích Siêu nhân","Thường" };
        model = (DefaultComboBoxModel) cboLoaiPhong.getModel();
        model.removeAllElements();
        for (int i = 0; i < loaiphong.length; i++) {
            model.addElement(loaiphong[i]);
        }
     }
    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachPhong.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtTimKiem.getText();
            List<PhongPK> list = dao.selectByKeyword(keyword);
            for(PhongPK p : list){
                Object[] row = {
                    p.getMaPhong(),
                    p.getTenPhong(),
                    p.getGiaPhong(),
                    p.getSoGiuong(),
                    p.getLoaiPhong()
                };
                    
                model.addRow(row);             
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
         
    void setForm(PhongPK p){
    txtMaPhong.setText(p.getMaPhong());
    txtTenPhong.setText(p.getTenPhong());
    txtGiaPhong.setText(String.valueOf(p.getGiaPhong()));
    txtSoGiuong.setText(String.valueOf(p.getSoGiuong()));
    cboLoaiPhong.setSelectedItem(p.getLoaiPhong());
    }
            
            
    void clearForm(){
        PhongPK nv = new PhongPK();
        this.setForm(nv);
        this.row = -1;
    }
    
    PhongPK getForm() {
        PhongPK p = new PhongPK();
        p.setMaPhong(txtMaPhong.getText());
        p.setTenPhong(txtTenPhong.getText());
        p.setGiaPhong(Float.valueOf(txtGiaPhong.getText()));
        p.setSoGiuong(Integer.valueOf(txtSoGiuong.getText()));
        p.setLoaiPhong(cboLoaiPhong.getSelectedItem().toString());
        return p;
    }
    
    
     void insert() {
        PhongPK model = this.getForm();
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "thêm mới thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "thêm mới thành công");
        }
    }

    void edit() {
        String map = (String) tblDanhSachPhong.getValueAt(this.row, 0);
        PhongPK p = dao.selectById(map);
        this.setForm(p);
//        NhanVien.setSelectedIndex(0);

    }

    void delete() {
        String mabs = txtMaPhong.getText();
        try {
            dao.delete(mabs);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Xóa thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
            MsgBox.alert(this, "Xóa thất bại!");
        }
    }

    void update() {
        PhongPK model = getForm();
        try {
            dao.update(model);
            this.fillTable();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, e);
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

