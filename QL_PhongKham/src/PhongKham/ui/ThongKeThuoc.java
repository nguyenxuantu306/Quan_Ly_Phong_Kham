    public void filltablethuocdabantheothang() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblThongKeT.getModel();
            String thangban = (String) thang.getSelectedItem();
            String namban = (String) nam.getSelectedItem();
            model.setColumnIdentifiers(header);
            model.setRowCount(0);
            List<Object[]> list = daotk.getSoLuongThuocDB(thangban, namban);
            for (Object[] objects : list) {
                model.addRow(objects);
            }
            tblThongKeT.setModel(model);
        } catch (Exception e) {
        }

    }

