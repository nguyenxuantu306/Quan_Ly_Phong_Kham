    public void filltablethuoconlai() {
        DefaultTableModel model = (DefaultTableModel) tblThongKeT.getModel();
        String header1[] = {"Mã thuốc", "Tên thuốc", "Số lượng", "Đơn vị"};
        model.setColumnIdentifiers(header1);
        model.setRowCount(0);
        List<Object[]> list = daotk.getLuongThuocConLai();
        for (Object[] objects : list) {
            model.addRow(objects);
        }
        tblThongKeT.setModel(model);
    }
    private void trangthaiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_trangthaiItemStateChanged
        // TODO add your handling code here:
        if (trangthai.getSelectedIndex() == 0) {
            filltablethuocdaban();
            path = "src/report/ThongKeThuoc_daban_all.jrxml";
            thang.setEditable(true);
            nam.setEditable(true);
        } else {
            filltablethuoconlai();
            path = "src/report/ThongKeThuoc_conlai.jrxml";
            thang.setSelectedIndex(0);
            nam.setSelectedIndex(0);
            thang.setEditable(false);
            nam.setEditable(false);
        }
    }//GEN-LAST:event_trangthaiItemStateChanged

