    private void cboChonTrangThaiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboChonTrangThaiItemStateChanged
        // TODO add your handling code here:
        int index = cboChonTrangThai.getSelectedIndex();
        DefaultTableModel model = (DefaultTableModel) tblDKDanhSach.getModel();
        if (index != 0) {
            model.setRowCount(0);
            String trangThai = (String) cboChonTrangThai.getSelectedItem();
            List<Object[]> chon = daodk.ChonTrangThai(trangThai);
            for (Object[] objects : chon) {
                model.addRow(objects);
            }
            tblDKDanhSach.setModel(model);
        }else{
            fillTableDK();
        }
    }//GEN-LAST:event_cboChonTrangThaiItemStateChanged

