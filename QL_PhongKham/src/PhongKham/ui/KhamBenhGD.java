    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblBenhAn.getModel();
        model.setRowCount(0);
        
        try {
            List<BenhAnPK> list = dao.selectAll();
            for(BenhAnPK ba : list){
                Object[] row = {
                   ba.getMaBA(),
                   ba.getHoTenBN(),
                   ba.getHoTenNV(),                   
                   XDate.toString(ba.getNgayKham(), "MM/dd/yyyy"),
                   ba.getDauHieuBenh(),
                   ba.getXetNghiem(),
                   ba.getChuanDoan(),
                   ba.getGhiChu(),
                   ba.getMaNV(),
                   ba.getMaBN(),
                   ba.getMaVP(),
                   ba.getMaLichKham(),
                   ba.getMaDT(),
                   ba.getMaPhong(),                 
                   };
                model.addRow(row);             
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    
        
}
    
     void fillTablee() {
        DefaultTableModel model = (DefaultTableModel) tblBenhNhanCho.getModel();
        model.setRowCount(0);
        try {
            List<PhieuKhamPK> list = daodao.selectAll();
            for(PhieuKhamPK pk : list){
                Object[] row = {                                             
                    pk.getDauHieuBenh(),                                     
                    pk.getHoTenBN(),
                    pk.getHoTenNV(),
                    pk.getMaPhong(),
                    pk.getMaBN(),
                    pk.getMaNV(),};                   
                model.addRow(row);             
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
